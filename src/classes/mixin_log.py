class MixinLogger:
    """Миксин для логирования создания объектов.

    Добавляет метод log_creation(), который выводит
    строку с техническим представлением объекта.

    Methods:
        log_creation(): Печатает сообщение с __repr__ объекта.
    """

    def log_creation(self) -> None:
        """
        Выводит сообщение о создании объекта.

        Использует метод __repr__(), который должен быть
        реализован в классе-наследнике.
        """
        class_name = self.__class__.__name__
        print(f"Создан объект класса {class_name} с параметрами: {self}")
